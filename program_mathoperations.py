# This is a simple program to do math operations in two optional numbers

x=input("Please, enter your first number:\n")
x=int(x)
y=input("Please, enter your second number:\n")
y=int(y)

print(f"{x}+{y}=", x+y)
print(f"{x}-{y}=", x-y)
print(f"{x}*{y}=", x*y)
print(f"{x}+{x}=", x/y)